/*Stored Procedure para buscar ventas en una fecha específica con control de errores*/

DELIMITER $$
CREATE PROCEDURE GetSalesByDate(IN input_date DATE)
BEGIN
	DECLARE row_count_sales INT;
    SELECT COUNT(*) INTO row_count_sales FROM sales WHERE purchase_date = input_date;
    IF row_count_sales = 0 THEN
		SIGNAL SQLSTATE "45000" SET MESSAGE_TEXT = "No sales found for the specified date";
	ELSE
    SELECT * FROM sales WHERE purchase_date = input_date;
    END IF;
END $$
DELIMITER ;

/*Para ejecutar el SP "GetSalesByDate" con fecha válida*/

CALL GetSalesByDate('2023-11-30');

/*Para ejecutar el SP "GetSalesByDate" con fecha inválida*/

CALL GetSalesByDate('2024-7-14');

/*Stored Procedure que almacena el conteo de ventas según la estación ingresada*/

DELIMITER $$
CREATE PROCEDURE CountSalesBySeason(IN input_season VARCHAR(6), OUT total_sales INT)
BEGIN
    SELECT COUNT(*)
    INTO total_sales
    FROM sales s
    JOIN calendar cal ON s.purchase_date = cal.purchase_date
    WHERE cal.season = input_season;
END $$
DELIMITER ;


/* Eliminamos la base de datos si ya existe */

DROP DATABASE IF EXISTS season_chic;

/* Creamos la base de datos */

CREATE DATABASE IF NOT EXISTS season_chic;

/* Usamos la base de datos especifica llamada "Season_Chic" */

USE season_chic;

/* A seguir, se crean las tablas, empezando con las tablas principales (padres) y después con las tablas secundarias (hijas) */

CREATE TABLE IF NOT EXISTS calendar 
(
	purchase_date DATE NOT NULL,
	year_date VARCHAR(4) NOT NULL,
   	 month_no VARCHAR(2) NOT NULL,
    	month_date VARCHAR(9) NOT NULL,
    	month_abb VARCHAR(4) NOT NULL,
   	quarter_date VARCHAR(2) NOT NULL,
    	weekday_date VARCHAR(9) NOT NULL,
    	season VARCHAR(6) NOT NULL,
    	PRIMARY KEY (purchase_date),
    	INDEX (month_no),
    	INDEX (season)
);

CREATE TABLE IF NOT EXISTS customers 
(
	customer_ID VARCHAR(4) NOT NULL,
	first_name VARCHAR(50), 
	last_name VARCHAR(50),
	email VARCHAR(100),
	age VARCHAR(3),
	gender VARCHAR(6),
	previous_purchases INT,
	frequency_purchases VARCHAR(15),
	PRIMARY KEY (customer_ID)
);

CREATE TABLE IF NOT EXISTS payment 
(
	payment_ID VARCHAR(6) NOT NULL,
	payment_method VARCHAR(13), 
	PRIMARY KEY (payment_ID)
);

CREATE TABLE IF NOT EXISTS shipping 
(
	shipping_ID VARCHAR(7) NOT NULL,
	shipping_type VARCHAR(15), 
	shipping_time INT,
	PRIMARY KEY (shipping_ID)
);

CREATE TABLE IF NOT EXISTS suppliers 
(
	supplier_ID VARCHAR(6) NOT NULL,
	supplier_name VARCHAR(100), 
	contact VARCHAR(100),
	PRIMARY KEY (supplier_ID)
);

CREATE TABLE IF NOT EXISTS stores 
(
	store_ID VARCHAR(7) NOT NULL,
	subsidiary VARCHAR(80), 
	location VARCHAR(15),
	region VARCHAR(10),
	PRIMARY KEY (store_ID)
);

CREATE TABLE IF NOT EXISTS categories 
(
	category_ID VARCHAR(3) NOT NULL,
	category_name VARCHAR(11), 
	PRIMARY KEY (category_ID)
);

CREATE TABLE IF NOT EXISTS invoices 
(
	invoice_ID VARCHAR(8) NOT NULL,
	invoice_type VARCHAR(27), 
	PRIMARY KEY (invoice_ID)
);

CREATE TABLE IF NOT EXISTS discounts 
(
	promo_code VARCHAR(8) NOT NULL,
	promo_description VARCHAR(30), 
	start_date_day VARCHAR(2) NOT NULL,
	start_date_month VARCHAR(2) NOT NULL,
	end_date_day VARCHAR(2) NOT NULL,
	end_date_month VARCHAR(2) NOT NULL,
	percent_discount FLOAT NOT NULL,
	PRIMARY KEY (promo_code),
    	CONSTRAINT fk_discounts_start_month FOREIGN KEY (start_date_month) REFERENCES calendar(month_no),
    	CONSTRAINT fk_discounts_end_month FOREIGN KEY (end_date_month) REFERENCES calendar(month_no)
);

CREATE TABLE IF NOT EXISTS salespeople 
(
	seller_ID VARCHAR(6) NOT NULL,
	first_name VARCHAR(50), 
	last_name VARCHAR(50), 
	store_ID VARCHAR(7) NOT NULL,
	PRIMARY KEY (seller_ID),
	CONSTRAINT fk_salespeople_store_ID FOREIGN KEY (store_ID) REFERENCES stores(store_ID)
);

CREATE TABLE IF NOT EXISTS sales 
(
	purchased_ID INT AUTO_INCREMENT NOT NULL,
	customer_ID VARCHAR(4) NOT NULL, 
	seller_ID VARCHAR(6) NOT NULL, 
    	store_ID VARCHAR(7) NOT NULL, 
	purchase_date DATE NOT NULL,
	purchased_amount_USD FLOAT,
	size VARCHAR(2),
	color VARCHAR(12),
	season VARCHAR(6) NOT NULL,   
	review_rating FLOAT,
	promo_code VARCHAR(8) NOT NULL, 
	promo_code_used BOOLEAN,
	PRIMARY KEY (purchased_ID),
	CONSTRAINT fk_sales_customer_ID FOREIGN KEY (customer_ID) REFERENCES customers(customer_ID),
	CONSTRAINT fk_sales_seller_ID FOREIGN KEY (seller_ID) REFERENCES salespeople(seller_ID),
    	CONSTRAINT fk_sales_store_ID FOREIGN KEY (store_ID) REFERENCES stores(store_ID),
    	CONSTRAINT fk_sales_purchase_date FOREIGN KEY (purchase_date) REFERENCES calendar(purchase_date),
    	CONSTRAINT fk_sales_season FOREIGN KEY (season) REFERENCES calendar(season),
	CONSTRAINT fk_sales_promo_code FOREIGN KEY (promo_code) REFERENCES discounts(promo_code)
);

CREATE TABLE IF NOT EXISTS products 
(
	SKU VARCHAR(7) NOT NULL,
	item VARCHAR(15),
	category_ID VARCHAR(3) NOT NULL, 
	unit_price_USD FLOAT,
	quantity_available INT,
	PRIMARY KEY (SKU),
	CONSTRAINT fk_products_category_ID FOREIGN KEY (category_ID) REFERENCES categories(category_ID)
);

CREATE TABLE IF NOT EXISTS inventory 
(
	inventory_ID VARCHAR(8) NOT NULL,
	SKU VARCHAR(7) NOT NULL, 
	quantity INT,
	sale_date DATE NOT NULL,
	store_ID VARCHAR(7) NOT NULL, 
	supplier_ID VARCHAR(6) NOT NULL, 
	size VARCHAR(2),
	color VARCHAR(12),
	season VARCHAR(6) NOT NULL,
	PRIMARY KEY (inventory_ID),
	CONSTRAINT fk_inventory_SKU FOREIGN KEY (SKU) REFERENCES products(SKU),
    	CONSTRAINT fk_sales_sale_date FOREIGN KEY (sale_date) REFERENCES calendar(purchase_date),
	CONSTRAINT fk_inventory_store_ID FOREIGN KEY (store_ID) REFERENCES stores(store_ID),
	CONSTRAINT fk_inventory_supplier_ID FOREIGN KEY (supplier_ID) REFERENCES suppliers(supplier_ID),
    	CONSTRAINT fk_inventory_season FOREIGN KEY (season) REFERENCES calendar(season)
);

CREATE TABLE IF NOT EXISTS return_purchase 
(
	return_ID VARCHAR(8) NOT NULL,
	purchased_ID INT NOT NULL, 
	SKU VARCHAR(7) NOT NULL, 
	reason VARCHAR(35), 
	return_date DATE NOT NULL,
	PRIMARY KEY (return_ID),
	CONSTRAINT fk_return_purchased_ID FOREIGN KEY (purchased_ID) REFERENCES sales(purchased_ID),
	CONSTRAINT fk_return_SKU FOREIGN KEY (SKU) REFERENCES products(SKU),
    	CONSTRAINT fk_sales_return_date FOREIGN KEY (return_date) REFERENCES calendar(purchase_date)
);

CREATE TABLE IF NOT EXISTS sales_facts 
(
	purchased_ID INT NOT NULL AUTO_INCREMENT,
	SKU VARCHAR(7) NOT NULL,
	customer_ID VARCHAR(4) NOT NULL,
	payment_ID VARCHAR(6) NOT NULL,
	shipping_ID VARCHAR(7) NOT NULL,
	invoice_ID VARCHAR(8) NOT NULL,
	quantity_purchased INT,
	unit_price_USD FLOAT,
	purchase_date DATE NOT NULL,
	PRIMARY KEY (purchased_ID),
	CONSTRAINT fk_facts_SKU FOREIGN KEY (SKU) REFERENCES products(SKU),
	CONSTRAINT fk_facts_customer_ID FOREIGN KEY (customer_ID) REFERENCES customers(customer_ID),
	CONSTRAINT fk_facts_payment_ID FOREIGN KEY (payment_ID) REFERENCES payment(payment_ID),
	CONSTRAINT fk_facts_shipping_ID FOREIGN KEY (shipping_ID) REFERENCES shipping(shipping_ID),
	CONSTRAINT fk_facts_invoice_ID FOREIGN KEY (invoice_ID) REFERENCES invoices(invoice_ID),
    	CONSTRAINT fk_facts_purchase_date FOREIGN KEY (purchase_date) REFERENCES calendar(purchase_date)
);


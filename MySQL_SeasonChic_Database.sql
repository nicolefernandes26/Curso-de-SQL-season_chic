/*Función de ventas por estación*/

DELIMITER $$
CREATE FUNCTION get_sales_by_season(year_param VARCHAR(4), season_param VARCHAR(6))
RETURNS DECIMAL(10, 2)
DETERMINISTIC
BEGIN
    DECLARE total_sales DECIMAL(10, 2);
    
    SELECT SUM(s.purchased_amount_USD) INTO total_sales
    FROM sales s
    JOIN calendar cal ON s.purchase_date = cal.purchase_date
    WHERE cal.year_date = year_param AND cal.season = season_param;
    
    RETURN IFNULL(total_sales, 0);
END $$
DELIMITER ;

/*Para ejecutar la función "get_sales_by_season"*/

SELECT get_sales_by_season('2021', 'Fall');

/*Función de total de inventario por producto*/

DELIMITER $$
CREATE FUNCTION get_total_inventory_by_item(item_param VARCHAR(50))
RETURNS INT
DETERMINISTIC
BEGIN
    DECLARE total_inventory INT;
    
    SELECT SUM(i.quantity) INTO total_inventory
    FROM Inventory i
    JOIN Products p ON i.SKU = p.SKU
    WHERE p.item = item_param;
    
    RETURN IFNULL(total_inventory, 0);
END $$
DELIMITER ;


/*Vista de Ventas por Producto y Categoría*/

CREATE VIEW sales_by_product_category AS
SELECT 
    p.SKU,
    p.item,
    c.category_name,
    SUM(s.purchased_amount_USD) AS total_sales_usd,
    SUM(sf.quantity_purchased) AS total_units_sold,
    COUNT(s.purchased_ID) AS total_transactions
FROM 
    Products p
JOIN 
    Categories c ON p.category_ID = c.category_ID
JOIN 
    Sales_Facts sf ON p.SKU = sf.SKU
JOIN 
    Sales s ON s.purchased_ID = sf.purchased_ID
GROUP BY 
    p.SKU, p.item, c.category_name;
    
/*Vista de Ventas con Descuentos Aplicados*/

CREATE VIEW sales_with_discounts AS
SELECT 
    s.purchased_ID,
    s.purchase_date,
    s.purchased_amount_USD,
    d.promo_code,
    d.promo_description,
    d.percent_discount,
    (s.purchased_amount_USD - (s.purchased_amount_USD * d.percent_discount)) AS total_after_discount
FROM 
    Sales s
JOIN 
    Discounts d ON s.promo_code = d.promo_code
WHERE 
    s.promo_code_used = TRUE;
    
/*Vista del inventario disponible por producto*/

CREATE VIEW inventory_by_product AS
SELECT 
    p.SKU, 
    p.item,
    p.unit_price_USD,
    SUM(i.quantity) AS total_quantity_available,
    SUM(i.quantity * p.unit_price_USD) AS total_value_usd
FROM 
    Products p
JOIN 
    Inventory i ON p.SKU = i.SKU
GROUP BY 
    p.SKU, p.item, p.unit_price_USD;
    
/*Vista de ventas por estación y año*/

CREATE VIEW sales_by_season_and_year AS
SELECT 
    cal.year_date AS year,
    cal.season AS season,
    SUM(s.purchased_amount_USD) AS total_sales_amount
FROM 
    Sales s
JOIN 
    Calendar cal ON s.purchase_date = cal.purchase_date
GROUP BY 
    cal.year_date, cal.season
ORDER BY 
    cal.year_date, cal.season;
    
/*Vista de ventas por fecha y región*/

CREATE VIEW sales_by_date_region AS
SELECT 
    cal.year_date,
    cal.month_date,
    st.region,
    SUM(s.purchased_amount_USD) AS total_sales_usd,
    COUNT(s.purchased_ID) AS total_transactions
FROM 
    Sales s
JOIN 
    Calendar cal ON s.purchase_date = cal.purchase_date
JOIN 
    Stores st ON s.store_ID = st.store_ID
GROUP BY 
	cal.year_date, cal.month_date, st.region;
 
